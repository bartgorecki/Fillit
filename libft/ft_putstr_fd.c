#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int	pos;

	if (s && fd)
	{
		pos = 0;
		while (s[pos])
		{
			ft_putchar_fd(s[pos], fd);
			pos++;
		}
	}
}
